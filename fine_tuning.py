def diagnose_pe_limitations():
    """
    诊断 PE + RAG 的三类顽固问题：
      1. 说教味太重 —— 用户要简洁，模型答长篇
      2. 不懂直播梗 —— 用户玩梗，模型回复像说明书
      3. 安全边界不稳 —— 该拒的不拒、该放的不放
    """
    print()
    print("╔" + "═" * 68 + "╗")
    print("║  Part A — 问题诊断：PE + RAG 的局限究竟在哪？         ║")
    print("║  用 5 个「陷阱问题」调用真实 RAG 管道，逐条分析        ║")
    print("╚" + "═" * 68 + "╝")

    # 加载 RAG Pipeline
    from rag_pipeline import RAGPipeline
    try:
        pipeline = RAGPipeline()
    except Exception as e:
        print(f"\n[跳过诊断] 无法加载 RAG Pipeline: {e}")
        print("请先运行 python main.py build 构建向量库\n")
        return []

    print(f"\n  模型: {pipeline.index.ntotal} 条向量 · 3 个商品知识库")
    print()

    results = []

    for i, q_data in enumerate(TRAP_QUESTIONS):
        qid = q_data["id"]
        question = q_data["question"]
        category = q_data["category"]
        problem_desc = q_data.get("problem", "")

        print(f"  {'─' * 66}")
        print(f"  [{qid}] 问题类别: {category}")
        print(f"  用户提问: 「{question}」")
        print(f"  PE 已知弱点: {problem_desc}")

        # 调用真实 RAG
        try:
            result = pipeline.answer(question, mode="rag")
            rag_answer = result["answer"]
            retrieved_count = len(result["retrieved_chunks"])
        except Exception as e:
            rag_answer = f"[调用失败: {e}]"
            retrieved_count = 0

        print(f"  ──────────────────────────────────────────────")
        print(f"  ↓ RAG 实际输出 ({retrieved_count} 条知识片段):")
        # 缩进打印回答
        for line in rag_answer.split("\n"):
            print(f"  │ {line.strip()}")
        print(f"  ──────────────────────────────────────────────")

        # 类别化分析
        if "说教味" in category:
            print(f"  ✗ 诊断: 说教味太重")
            print(f"     → 用户要求简洁，输出可能偏长/偏正式")
            print(f"     → PE 模板只能控制'风格指令'，无法根本改变模型的")
            print(f"       深层语气习惯（这是预训练阶段学到的'百科问答体'）")
        elif "直播梗" in category:
            print(f"  ✗ 诊断: 不懂直播梗 / 接不住情绪")
            print(f"     → 用户使用了「绝绝子」「蹲」等直播黑话")
            print(f"     → PE 可以教模型'用直播语气'，但永远无法让它真正理解")
            print(f"       「绝绝子」在直播语境下的情绪浓度和互动期待")
        elif "过于保守" in category:
            print(f"  ✗ 诊断: 安全边界过于保守")
            print(f"     → 面对「孕期」关键词，模型的安全对齐(RLHF)可能触发")
            print(f"       过度保护，即使知识库明确写了「可以」也拒绝给出结论")
            print(f"     → PE 无法覆盖 RLHF 阶段的深层偏好")
        elif "过于激进" in category:
            print(f"  ✗ 诊断: 安全边界可能不够稳")
            print(f"     → 面对用户质疑，模型可能过度辩解/承诺超出文档范围的效果")
            print(f"     → PE 可以加约束词，但约束词会和'要有说服力'产生内部冲突")
        elif "多层" in category or "混合" in category:
            print(f"  ✗ 诊断: 多层问题处理能力不足")
            print(f"     → 用户问了多个子问题 + 格式要求")
            print(f"     → PE 模板是扁平指令，无法调度多步推理节奏")

        results.append({
            "id": qid,
            "category": category,
            "question": question,
            "rag_answer": rag_answer,
            "problem": problem_desc,
        })

        time.sleep(0.3)  # 避免压垮 Ollama

    return results


